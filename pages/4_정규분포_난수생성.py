st.markdown("""
## 📚 개념 정리

### 정규분포 N(0,1) 난수 생성이란?
표준정규분포(평균 0, 분산 1)를 따르는 랜덤한 숫자를 생성하는 방법입니다.

### 핵심 용어
1. **균일분포(Uniform Distribution)**: 0과 1 사이의 모든 값이 동일한 확률로 나타나는 분포
2. **누적분포함수(CDF)**: 확률변수가 특정 값보다 작거나 같을 확률을 나타내는 함수
3. **역함수(Quantile Function)**: CDF의 역함수로, 확률값에 대응하는 확률변수 값을 반환
""")

st.markdown("""
## 📝 시험 답안 작성법

### 1. 기본 답안 구조
```
정규분포 N(0,1)을 따르는 랜덤한 숫자를 만드는 방법은 다음과 같다:

1. 0과 1 사이의 균일분포에서 난수 u를 생성한다.
2. 이 난수를 정규분포의 누적분포함수(CDF)의 역함수에 대입한다.
3. x = Φ⁻¹(u)를 계산한다.
4. 결과값 x는 N(0,1) 정규분포를 따른다.

이 방법을 역변환 표본추출법(Inverse Transform Sampling)이라고 한다.
```

### 2. 수식 포함 답안 (가산점용)
```
정규분포 N(0,1) 난수 생성 과정:

1. u ~ U(0,1) 균일분포에서 난수 추출
2. x = Φ⁻¹(u) 계산
   여기서 Φ⁻¹은 표준정규분포의 역누적분포함수

결과적으로 x ~ N(0,1) 성립
```
""")

st.markdown("""
## 🎯 채점 포인트

### 필수 포함 요소
1. **균일분포 언급**
   - 0과 1 사이
   - U(0,1) 표기 가능

2. **누적분포함수(CDF)**
   - 'CDF' 또는 '누적분포함수' 용어 사용
   - Φ(x) 표기 가능

3. **역함수(Quantile)**
   - '역함수' 또는 'Quantile function' 용어 사용
   - Φ⁻¹ 표기 가능

### 가산점 요소
1. **방법론 명칭**
   - 역변환 표본추출법
   - Inverse Transform Sampling

2. **수식 표현**
   - x = Φ⁻¹(u)
   - u ~ U(0,1)
   - x ~ N(0,1)
""")

